package com.example.chelvan.neon_uom;

public class NewsFeedData {
    public String  title;
    public String msg;
    public String routenum;
    public String time;
    public String date;
    public String name;


    public NewsFeedData(String title,String msg,String routenum,String time,String date,String name){
        this.title = title;
        this.msg = msg;
        this.routenum = routenum;
        this.time = time;
        this.date = date;
        this.name = name;
    }

}
